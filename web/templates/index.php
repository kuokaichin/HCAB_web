			For Our Buddies
		</div>

		<div class="aindent">
			<img src="HCAB/images/singleflower.png" class="rfl"/>
			Our committed volunteers <highlight>build friendships</highlight> through their regular visits 
			and a trust that can inspire hope.We arrive with energy, a loving smile, and a caring touch, 
			wanting to really know our buddies.
		</div>	
	
		<div class="atindent">
			For Solidarity
		</div>
		
		<div class="aindent">
			<img src="HCAB/images/singleflower.png" class="lfl"/>
			College students and dementia patients are not that different- they and their <highlight>families</highlight> are both 
			undergoing major transitions in their lives during which they must reassess priorities and cope with new challenges. 
			With dementia, the transitions can be devastating. Together we work to find the <highlight>courage</highlight> to face these challenges.
		</div>
		
		<div class="atindent">
			For the Staff
		</div>

		<div class="alindent">
			<img src="HCAB/images/singleflower.png" class="rfl"/>
			We help to relax and engage our buddies, leading them to be more <highlight>cooperative</highlight> with the support staff. 
			Our visits provide a relief for nursing staff so that they can focus on their care-giving duties.
		</div>

