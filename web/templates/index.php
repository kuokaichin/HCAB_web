		<br />
		
                <div class="aindent">
                        <br />
                        <br />
			Sometimes visiting with dementia victims can be difficult. We <highlight>support</highlight> our volunteers with an
			extensive training program with the help of healthcare professionals. This training is 
			aimed at both improving their weekly visits and overall understanding
			of how families work to cope with Alzheimer's. We view this education as essential, not only
			because it is likely that our volunteers will encounter someone with dementia during their lives, but	
			also because they need to be able to connect to their buddies' loved ones.
