       		<div class="aindent">
			<br />
			<br />
			<br />
			<br />
			Undergraduates  responding to the<highlight> profound isolation</highlight> and<highlight> 
			social disengagement</highlight> experienced by people in the intermediate-to-late-stages 
			of Alzheimer's disease (AD). We build meaningful emotional connections with dementia patients weekly 
			one-on-one visits. We focus on what is still present in our buddies and improving the life that they have despite limitations. 
		</div>

